package main

import (
	"github.com/tbxark/options-proto/go/tbxark/options"
	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"
	"google.golang.org/protobuf/types/descriptorpb"
)

const (
	botAnnotationKey   = "bot"
	deprecationComment = "// Deprecated: Do not use."
)

const (
	contextPackage = protogen.GoImportPath("context")
)

var methodSets = make(map[string]int)

func generateFile(gen *protogen.Plugin, file *protogen.File, conf *Config) *protogen.GeneratedFile {
	if len(file.Services) == 0 || (!hasBotRule(file.Services)) {
		return nil
	}
	filename := file.GeneratedFilenamePrefix + "_bot.pb.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)
	g.P("// Code generated by protoc-gen-bot. DO NOT EDIT.")
	g.P("// versions:")
	if file.Proto.GetOptions().GetDeprecated() {
		g.P("// ", file.Desc.Path(), " is a deprecated file.")
	} else {
		g.P("// source: ", file.Desc.Path())
	}
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()
	generateFileContent(gen, file, g, conf)
	return g
}

func generateFileContent(gen *protogen.Plugin, file *protogen.File, g *protogen.GeneratedFile, conf *Config) {
	if len(file.Services) == 0 {
		return
	}
	g.P("var _ = new(", contextPackage.Ident("Context"), ")")
	g.P("var _ = new(", conf.botPackage.Ident(conf.requestType), ")")
	g.P()
	for _, service := range file.Services {
		genService(gen, file, g, service, conf)
	}
}

func genService(_ *protogen.Plugin, file *protogen.File, g *protogen.GeneratedFile, service *protogen.Service, conf *Config) {
	if service.Desc.Options().(*descriptorpb.ServiceOptions).GetDeprecated() {
		g.P("//")
		g.P(deprecationComment)
	}
	// Bot Server.
	sd := &serviceDesc{
		ServiceType: service.GoName,
		ServiceName: string(service.Desc.FullName()),
		Metadata:    file.Desc.Path(),

		RequestType:      g.QualifiedGoIdent(conf.botPackage.Ident(conf.requestType)),
		ResponseType:     g.QualifiedGoIdent(conf.botPackage.Ident(conf.responseType)),
		ExtraDataType:    g.QualifiedGoIdent(conf.botPackage.Ident(conf.extraType)),
		NewExtraDataFunc: g.QualifiedGoIdent(conf.botPackage.Ident(conf.extraConstructor)),
	}
	for _, method := range service.Methods {
		rule := extractBotRule(method)
		if rule == nil {
			continue
		}
		sd.Methods = append(sd.Methods, &methodDesc{
			Name:         method.GoName,
			OriginalName: string(method.Desc.Name()),
			Num:          methodSets[method.GoName],
			Request:      method.Input.GoIdent.GoName,
			Reply:        method.Output.GoIdent.GoName,
			Comment:      method.Comments.Leading.String(),
			Extra:        rule.Extra,
		})
		methodSets[method.GoName] += 1
	}
	if len(sd.Methods) != 0 {
		g.P(sd.execute())
	}
}

func hasBotRule(services []*protogen.Service) bool {
	for _, service := range services {
		for _, method := range service.Methods {
			if extractBotRule(method) != nil {
				return true
			}
		}
	}
	return false
}

func extractBotRule(method *protogen.Method) *options.KeyValuePair {
	if method.Desc.IsStreamingClient() || method.Desc.IsStreamingServer() {
		return nil
	}
	rules, ok := proto.GetExtension(method.Desc.Options(), options.E_Options).([]*options.KeyValuePair)
	if rules == nil || !ok {
		return nil
	}
	for _, rule := range rules {
		if rule.GetKey() == botAnnotationKey {
			return rule
		}
	}
	return nil
}
